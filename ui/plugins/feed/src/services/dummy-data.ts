const genTime = () => {
  const startDate = new Date('Mon Jun 03 2019 12:12:58 GMT+0300');
  const end = Date.now() / 1000;
  const start = startDate.getTime() / 1000;
  const date = new Date(start + Math.max(0, Math.random() * (end - start)));
  return date.getTime();
};

